#!/usr/bin/env python3
"""
02_generate_visualizations.py
=============================
Interactive script to generate trade-off visualizations
(Accuracy vs. Latency, Parameter Radar Charts) based on
the benchmark telemetry generated by `01_run_benchmark.py`.

Run directly via python: `python 02_generate_visualizations.py`
"""

import argparse
import sys
import json
import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import numpy as np
from pathlib import Path

# Paths
RESULTS_FILE = Path("results/benchmark_results.json")
FIGURES_DIR = Path("results/figures")

# Modern Visual Theme
sns.set_theme(style="darkgrid", context="paper")
plt.rcParams.update({
    "font.family": "sans-serif",
    "figure.facecolor": "#0d1320",
    "axes.facecolor": "#0a0e17",
    "axes.edgecolor": "#1f2937",
    "axes.labelcolor": "#e5e7eb",
    "text.color": "#e5e7eb",
    "xtick.color": "#9ca3af",
    "ytick.color": "#9ca3af",
    "grid.color": "#1f2937",
})


def load_data():
    if not RESULTS_FILE.exists():
        print(f"Error: {RESULTS_FILE} not found!")
        print("Please run `python 01_run_benchmark.py` first to generate telemetry.")
        sys.exit(1)
        
    with open(RESULTS_FILE, "r") as f:
        return json.load(f)


def create_accuracy_latency_scatter(models_data: dict, output_path: Path):
    """Generates scatter plot: Accuracy vs Latency with bubble size as model size."""
    data = []
    for key, metrics in models_data.items():
        data.append({
            "Model": metrics.get("model_info", {}).get("param_str", key),
            "Architecture": key,
            "Latency (ms)": metrics.get("latency_cpu", {}).get("mean_ms", 0),
            "Top-1 Accuracy (%)": metrics.get("accuracy", {}).get("top1_accuracy", 0),
            "Size (MB)": metrics.get("model_info", {}).get("size_mb", 10),
        })

    df = pd.DataFrame(data)
    if df.empty or df["Latency (ms)"].sum() == 0:
        print("Error: Incomplete latency/accuracy data found.")
        return

    plt.figure(figsize=(10, 6))
    scatter = sns.scatterplot(
        data=df,
        x="Latency (ms)",
        y="Top-1 Accuracy (%)",
        size="Size (MB)",
        hue="Architecture",
        sizes=(100, 1000),
        alpha=0.8,
        palette="husl",
        edgecolor="#ffffff",
    )

    for i in range(len(df)):
        plt.annotate(
            df["Model"].iloc[i],
            (df["Latency (ms)"].iloc[i], df["Top-1 Accuracy (%)"].iloc[i]),
            xytext=(10, -5),
            textcoords="offset points",
            fontsize=9,
            color="#e5e7eb",
            fontweight="bold"
        )

    plt.title("Edge Deployment Trade-offs: Accuracy vs. CPU Latency", fontsize=14, pad=15)
    plt.xlabel("Inference Latency (ms) [Lower is Better]")
    plt.ylabel("EuroSAT Aerial Top-1 Accuracy (%) [Higher is Better]")
    
    # Minimal legend
    handles, labels = scatter.get_legend_handles_labels()
    plt.legend(handles[-4:], labels[-4:], title="Model Size (MB)", loc='lower right', frameon=True, facecolor='#0a0e17')

    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches="tight", facecolor=plt.gcf().get_facecolor())
    plt.close()


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--silent", action="store_true", help="Run without interactive prompts")
    args = parser.parse_args()

    if not args.silent:
        os.system('cls' if os.name == 'nt' else 'clear')
        print("======================================================")
        print(" 📊 VISUALIZATION GENERATOR                           ")
        print("======================================================\n")
        print("This will parse your latest benchmark telemetry and build")
        print("PDF/PNG charts suitable for reporting & GitHub.")
        input("\nPress ENTER to generate visuals...")

    import os
    print("\n→ Loading telemetry data...")
    data = load_data()
    models = data.get("models", {})
    
    FIGURES_DIR.mkdir(parents=True, exist_ok=True)
    
    scatter_path = FIGURES_DIR / "accuracy_vs_latency.png"
    
    print("→ Rendering Accuracy vs Latency Scatter Plot...")
    create_accuracy_latency_scatter(models, scatter_path)

    print("\n✅ All visualizations generated successfully!")
    print(f"   Saved to: {FIGURES_DIR.absolute()}")


if __name__ == "__main__":
    main()
